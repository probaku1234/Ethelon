class User {
    constructor(id, firstName, lastName, email, imageUrl, description, title, phone, userId) {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.imageUrl = imageUrl;
        this.description = description;
        this.title = title;
        this.phone = phone;
        this.userId = userId;
    }
}

export default User;